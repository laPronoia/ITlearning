/*
How to conver data type
*/

public class ConvertingData {

  public static void main(String[] args) {


      String num = "1";

          // String to Interger
          System.out.println( Integer.parseInt(num)); // 1

          // String to Double
          System.out.println( Double.parseDouble(num)); // 1.0

          // String to boolean
          System.out.println( Boolean.parseBoolean(num)); // false

    }
}
